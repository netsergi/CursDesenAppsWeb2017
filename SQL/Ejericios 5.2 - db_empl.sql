SELECT EMP.EMPL_FORNAME, EMP.EMPL_SURNAME, OFC.OFFC_CITY, KNW.KNLN_NAME, EMP.EMPL_MENTOR_ID
FROM T_EMPLOYEES EMP
INNER JOIN t_offices OFC
		   ON EMP.OFFC_ID = OFC.OFFC_ID
LEFT OUTER JOIN T_KNOWLEDGE_LINES KNW
		   ON EMP.KNLN_ID = KNW.KNLN_ID
WHERE 
OFC.OFFC_COUNTRY IN ('Espa√±a','Chile') 
AND EMP.EMPL_MENTOR_ID IS NULL;

