SELECT P.PRJT_CODE 'CODI', P.PRJT_NAME 'NOM PROJECTE', 
E.EMPL_NUMBER 'NUMERO EMPLEAT', E.EMPL_SURNAME 'COGNOM EMPLEAT'
FROM T_PROJECTS P
INNER JOIN T_PROJECTS_EMPLOYEES PE
		   ON PE.PRJT_ID = P.PRJT_ID
INNER JOIN T_EMPLOYEES E
		   ON E.EMPL_ID = PE.EMPL_ID
WHERE E.EMPL_FORNAME = 'Juan';